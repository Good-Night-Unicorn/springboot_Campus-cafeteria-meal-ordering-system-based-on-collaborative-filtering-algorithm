<script>
  export default {
    data() {
      return {
        id: '',
        baseUrl: '',
        ro:{
            name : false,
            picture : false,
            address : false,
            score : false,
            price : false,
            recommentcount : false,
            laiyuan : false,
            dianpin : false,
            description : false,
            phone : false,
            caixi : false,
            yinyetime : false,
        },
        type: '',
        userTableName: localStorage.getItem('UserTableName'),
        ruleForm: {
          name: '',
          picture: '',
          address: '',
          score: '',
          price: '',
          recommentcount: '',
          laiyuan: '',
          dianpin: '',
          description: '',
          phone: '',
          caixi: '',
          yinyetime: '',
        },


        rules: {
          name: [
          ],
          picture: [
          ],
          address: [
          ],
          score: [
            { validator: this.$validate.isNumber, trigger: 'blur' },
          ],
          price: [
            { validator: this.$validate.isNumber, trigger: 'blur' },
          ],
          recommentcount: [
            { validator: this.$validate.isIntNumer, trigger: 'blur' },
          ],
          laiyuan: [
          ],
          dianpin: [
          ],
          description: [
          ],
          phone: [
          ],
          caixi: [
          ],
          yinyetime: [
          ],
        },
		centerType: false,
      };
    },
    computed: {



    },
    components: {
    },
    created() {
		if(this.$route.query.centerType){
			this.centerType = true
		}
	  //this.bg();
      let type = this.$route.query.type ? this.$route.query.type : '';
      this.init(type);
      this.baseUrl = this.$config.baseUrl;
    },
    methods: {
      getMakeZero(s) {
          return s < 10 ? '0' + s : s;
      },
      // 下载
      download(file){
        window.open(`${file}`)
      },
      // 初始化
      init(type) {
        this.type = type;
        if(type=='cross'){
          var obj = JSON.parse(localStorage.getItem('crossObj'));
          for (var o in obj){
            if(o=='name'){
              this.ruleForm.name = obj[o];
              this.ro.name = true;
              continue;
            }
            if(o=='picture'){
              this.ruleForm.picture = obj[o].split(",")[0];
              this.ro.picture = true;
              continue;
            }
            if(o=='address'){
              this.ruleForm.address = obj[o];
              this.ro.address = true;
              continue;
            }
            if(o=='score'){
              this.ruleForm.score = obj[o];
              this.ro.score = true;
              continue;
            }
            if(o=='price'){
              this.ruleForm.price = obj[o];
              this.ro.price = true;
              continue;
            }
            if(o=='recommentcount'){
              this.ruleForm.recommentcount = obj[o];
              this.ro.recommentcount = true;
              continue;
            }
            if(o=='laiyuan'){
              this.ruleForm.laiyuan = obj[o];
              this.ro.laiyuan = true;
              continue;
            }
            if(o=='dianpin'){
              this.ruleForm.dianpin = obj[o];
              this.ro.dianpin = true;
              continue;
            }
            if(o=='description'){
              this.ruleForm.description = obj[o];
              this.ro.description = true;
              continue;
            }
            if(o=='phone'){
              this.ruleForm.phone = obj[o];
              this.ro.phone = true;
              continue;
            }
            if(o=='caixi'){
              this.ruleForm.caixi = obj[o];
              this.ro.caixi = true;
              continue;
            }
            if(o=='yinyetime'){
              this.ruleForm.yinyetime = obj[o];
              this.ro.yinyetime = true;
              continue;
            }
          }
        }else if(type=='edit'){
			this.info()
		}
      },

    // 多级联动参数
      // 多级联动参数
      info() {
        this.$http.get(`meishi/detail/${this.$route.query.id}`, {emulateJSON: true}).then(res => {
          if (res.data.code == 0) {
            this.ruleForm = res.data.data;
          }
        });
      },
      // 提交
      onSubmit() {

        //更新跨表属性
        var crossuserid;
        var crossrefid;
        var crossoptnum;
        this.$refs["ruleForm"].validate(valid => {
          if(valid) {
            if(this.type=='cross'){
                 var statusColumnName = localStorage.getItem('statusColumnName');
                 var statusColumnValue = localStorage.getItem('statusColumnValue');
                 if(statusColumnName && statusColumnName!='') {
                     var obj = JSON.parse(localStorage.getItem('crossObj'));
                     if(!statusColumnName.startsWith("[")) {
                         for (var o in obj){
                             if(o==statusColumnName){
                                 obj[o] = statusColumnValue;
                             }
                         }
                         var table = localStorage.getItem('crossTable');
                         this.$http.post(table+'/update', obj).then(res => {});
                     } else {
                            crossuserid=Number(localStorage.getItem('userid'));
                            crossrefid=obj['id'];
                            crossoptnum=localStorage.getItem('statusColumnName');
                            crossoptnum=crossoptnum.replace(/\[/,"").replace(/\]/,"");
                     }
                 }
            }
            if(crossrefid && crossuserid) {
                 this.ruleForm.crossuserid=crossuserid;
                 this.ruleForm.crossrefid=crossrefid;
                 var params = {
                     page: 1,
                     limit: 10,
                     crossuserid:crossuserid,
                     crossrefid:crossrefid,
                 }
                 this.$http.get('meishi/list', {
                  params: params
                 }).then(res => {
                     if(res.data.data.total>=crossoptnum) {
                         this.$message({
                          message: localStorage.getItem('tips'),
                          type: 'success',
                          duration: 1500,
                         });
                          return false;
                     } else {
                         // 跨表计算


                          this.$http.post(`meishi/${this.ruleForm.id?'update':this.centerType?'save':'add'}`, this.ruleForm).then(res => {
                              if (res.data.code == 0) {
                                  this.$message({
                                      message: '操作成功',
                                      type: 'success',
                                      duration: 1500,
                                      onClose: () => {
                                          this.$router.go(-1);
                                      }
                                  });
                              } else {
                                  this.$message({
                                      message: res.data.msg,
                                      type: 'error',
                                      duration: 1500
                                  });
                              }
                          });
                     }
                 });
             } else {


                  this.$http.post(`meishi/${this.ruleForm.id?'update':this.centerType?'save':'add'}`, this.ruleForm).then(res => {
                     if (res.data.code == 0) {
                          this.$message({
                              message: '操作成功',
                              type: 'success',
                              duration: 1500,
                              onClose: () => {
                                  this.$router.go(-1);
                              }
                          });
                      } else {
                          this.$message({
                              message: res.data.msg,
                              type: 'error',
                              duration: 1500
                          });
                      }
                  });
             }
          }
        });
      },
      // 获取uuid
      getUUID () {
        return new Date().getTime();
      },
      // 返回
      back() {
        this.$router.go(-1);
      },
      pictureUploadChange(fileUrls) {
          this.ruleForm.picture = fileUrls.replace(new RegExp(this.$config.baseUrl,"g"),"");;
      },
    }
  };
</script>

